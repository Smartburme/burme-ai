export const getIconForFile = (name: string, type: 'file' | 'folder'): React.FC<IconProps> => {
    if (type === 'folder') return FolderIcon;
    if (name.endsWith('.js') || name.endsWith('.jsx') || name.endsWith('.ts') || name.endsWith('.tsx')) return JsIcon;
    if (name.endsWith('.json')) return JsonIcon;
    if (name.toLowerCase().includes('readme')) return ReadmeIcon;
    if (name === '.gitignore' || name === '.env') return GitignoreIcon;
    return FileIcon;
};

